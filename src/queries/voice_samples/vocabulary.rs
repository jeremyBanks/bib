use std::collections::BTreeSet;

pub fn english() -> BTreeSet<&'static str> {
    let mut words = BTreeSet::new();

    for source in &[
        PRIMITIVES,
        THING_EXPLAINER,
        ALPHABETS,
        GOOGLE_2006,
        US_NAMES_2010_2019,
        US_NAMES_2010_2019,
        UK_NAMES_2021,
        SHRUG,
    ] {
        words.extend(
            source
                .split_ascii_whitespace()
                .filter(|s| !s.is_empty()),
        )
    }

    words
}

